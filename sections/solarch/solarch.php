    <p>In my web design 3 class, I teamed up with three other web designers to create a new website for a local charity. The first action we took was to come up with a team name and a logo. We decided that we wanted to represent ourselves as solutions architects for the web, so we chose the name “Solarch”.</p>
    
    <?php single_photo($section_name, false, 'Solarch-font-study.png', 720, 'centre', 'I felt that the word “architect” called for a strong, structured font, so I began experimenting with them.'); ?>
    
    <p>I began a study to come up with my own concept for the logo, experimenting with design elements to create a logo that represented the concept behind the name. I worked first with a font, the backbone of a brand. Although many candidates worked well with the name, I felt that Amplitude’s bulky, squarish elements had the most potential.</p>
    
    <?php single_photo($section_name, false, 'Solarch-logotype-study.png', 720, 'centre', 'My decision in this stage was important because the structure of the logo determines how it can be implemented in other designs later on.'); ?>
    
    <p>Next I determined the structure and density I wanted the logo to have. In discussions with the team, we identified the compass as a tool that evoked architecture the most strongly, so I favoured the stepped layout to allow for interesting compositions with a compass graphic.</p>
    
    <?php single_photo($section_name, false, 'Solarch-logomark-study.png', 720, 'centre', 'Using vector shapes, I was able to create a compass graphic that could be manipulated to different angles.'); ?>
    
    <p>I constructed a compass graphic based on an image from the Internet, and created several compositions with the compass having varying degrees of involvment with the logotype. At the end I realized that the A was in the perfect position to be combined with the compass, allowing the compass to both divide the syllables of the name and evoke the A in its form.</p>
    
    <?php single_photo($section_name, false, 'Solarch-colour-study.png', 720, 'centre', 'Blue is the quinessential colour to imply stability and professionalism.'); ?>
    
    <p>I picked a vivid medium blue to reinforce the idea of professionalism, but I did a bit more experimentation to determine which arrangement of colours emphasized the structure most clearly. In the end, I felt that limiting the bright, saturated blue to “Sol” and differentiating “Arch” with a different shade of gray was imporant to reinforce the “A” structure of the compass while letting the whole logotype be clearly differentiated.</p>
    
