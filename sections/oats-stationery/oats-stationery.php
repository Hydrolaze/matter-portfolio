        <p>After I created advertisements for Optometry at the Shops, an optometric practice in Mission Viejo, they came to Mouse Graphics once again to have me design for them. This time they needed a suite of stationery, revamped to match the branding in their new ads. I was provided examples of their previous stationery to reference; letterhead, envelopes, business cards, a recall mailer card, and cards to write prescriptions and appointments on.</p>

        <!-- single_photo of the old OATS logo on a piece of stationery -->

        <span class="caption">The brand styling I created for the ads needed to be modified for stationery, where the color-blocked backgrounds would have to be eschewed for blank, writable space.</span>
        
        <p>To start off, I measured the past stationery examples and re-typeset all of the text in Illustrator. Referencing the brand fonts and colors I established in my ads for the practice <!-- link to mall-ads -->, I restructured the information from the old stationery into new designs that grouped relevant information and used variations in type and color to imply hierarchy.</p> 

        <!-- single_photo of simple card design beside old version -->
        
        <p>The horizontal stripes in the logo were an interesting design element to work with, and their rounded caps served as a recognizable design feature that I could evoke in various places in my designs. I mostly used them to accent lines of text, but I found that they could also be used to solve a design problem I was having.</p>
        
        <p>The underline text fields on some items were giving me trouble, as the limited space was forcing me to cram them closely together. They looked confusing and awkward scattered around among the text, so I replaced them the rounded bars from the logo. Since they had both top and bottom lines around the writing field, they gave the forms on the appointment and prescription cards more structure and balance. I also feel like it's easier to write neatly when one can position their letters between two lines. This is essential, since the cards are being used by doctors, who have notoriously unreadable handwriting.</p>
        
